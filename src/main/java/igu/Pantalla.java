// crear vehiculo
public String[][] guardarVehiculo(String[][] inventario) {
    int item = inventario.length;

    // Crear una nueva matriz de tamaño +1
    String[][] newInventario = new String[item + 1][inventario[0].length];
    
    // Copiar los elementos existentes a la nueva matriz
    for (int i = 0; i < item; i++) {
        System.arraycopy(inventario[i], 0, newInventario[i], 0, inventario[i].length);
    }
    // Agregar el nuevo vehículo
    newInventario[item][0] = Integer.toString(item + 1);
    newInventario[item][1] = String.valueOf(tipo.getSelectedItem());
    newInventario[item][2] = marca.getText();
    newInventario[item][3] = Modelo.getText();
    newInventario[item][4] = ano.getText();
    newInventario[item][5] = color.getText();
    newInventario[item][6] = kilometraje.getText();
    newInventario[item][7] = capacidad.getText();
    newInventario[item][8] = transmi.getText();
    newInventario[item][9] = String.valueOf(combustible.getSelectedItem());
    newInventario[item][10] = puertas.getText();
    newInventario[item][11] = descripcion.getText();
    newInventario[item][12] = precio.getText();
    newInventario[item][13] = stock.getText();

    return newInventario;
}

