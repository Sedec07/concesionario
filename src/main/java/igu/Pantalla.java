//llenar valores del inventario
public void datos (String[][] inventario_1) {
    DefaultTableModel model = (DefaultTableModel) tabla.getModel();
    // Limpiar el modelo actual
    model.setRowCount(0); // Esto elimina todas las filas actuales
    // Agregar las filas del inventario a la tabla
    for (int i = 0; i < inventario_1.length; i++) {
        // Crear un arreglo de objetos para cada fila
        Object[] rowData = new Object[inventario_1[i].length];
        for (int j = 0; j < inventario_1[i].length; j++) {
            rowData[j] = inventario_1[i][j]; // Asignar cada valor
        }
        model.addRow(rowData); // Agregar la fila al modelo
    }
}
